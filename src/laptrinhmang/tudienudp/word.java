public class word {
    private String EN, VN;

    public word() {
    }

    
    
    public word(String EN, String VN) {
        this.EN = EN;
        this.VN = VN;
    }
    
    public String getEN() {
        return EN;
    }

    public String getVN() {
        return VN;
    }

    public void setEN(String EN) {
        this.EN = EN;
    }

    public void setVN(String VN) {
        this.VN = VN;
    }
    
    
}
